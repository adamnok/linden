package linden.plugin.utils

import xsbti.Logger

object Log {

  implicit class ImplicitLogger(val logger: Logger) extends AnyVal {
    def asScala = new Log(logger)
  }

}

class Log(log: Logger) {
  def info(text: => String) = log.info(() => text)

  def error(text: => String) = log.error(() => text)

  def debug(text: String) = log.debug(() => text)
}
